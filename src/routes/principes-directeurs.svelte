<section>
	<p>Cette section vise à présenter une synthèse des recommandations qui émanent de la démarche de caractérisation des paysages et de l’exercice d’idéation de Saint-Constant. Elle prend la forme d’une charte des principes directeurs pour l’élaboration d’un projet de paysage. Les valeurs principales qui ont conduit à l’élaboration de cette charte sont&nbsp;:</p>
	<ul>
		<li>Une ville pour tous;</li>
		<li>Une ville de proximité;</li>
		<li>Une ville de nature;</li>
		<li>Une ville ancrée dans son territoire agricole.</li>
	</ul>
</section>
<section>
	<p>Ces valeurs ont été mises en action à travers trois orientations d’aménagements&nbsp;:</p>
	<ul>
		<li>Le développement d’un agroparc visant à créer des interconnexions entre le milieu urbain et agricole, illustré par la revitalisation du noyau villageois;</li>
		<li>L’aménagement de promenades citoyennes visant à créer des parcours de mobilités actives continus, fonctionnels, écologiques et conviviaux à travers le territoire de la municipalité;</li>
		<li>Le développement de pôle de mixité et de densification capables de s’intégrer au contexte urbain actuel.</li>
	</ul>
</section>
<section>
	<p>Toutes ces propositions visent à révéler et mettre en valeur les principaux éléments identitaires des paysages de Saint-Constant, à savoir&nbsp;:</p>
	<ul>
		<li>Les abords de la rivière Saint-Pierre et Saint-Régis;</li>
		<li>La connexion diagonale de la ligne de transport électrique;</li>
		<li>La proximité entre le noyau villageois et le milieu agricole;</li>
		<li>Les axes de densification de la route 132, de la rue Saint-Pierre, de la montée Saint-Régis et du chemin Sainte-Catherine.</li>
	</ul>
</section>
